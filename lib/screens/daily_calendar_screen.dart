        children: [
          // ‡∏™‡πà‡∏ß‡∏ô‡∏´‡∏±‡∏ß (‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏∑‡∏≠‡∏Å View Mode) ‡∏à‡∏∞‡∏≠‡∏¢‡∏π‡πà‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡πÄ‡∏î‡∏¥‡∏°
